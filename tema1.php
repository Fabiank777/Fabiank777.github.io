        <h1 class="titulo" id="ancla">Elipse</h1>

        <p>Una elipse es una curva geométrica que se encuentra dentro del conjunto de las secciones cónicas, junto con el círculo, la parábola y la hipérbola. En términos sencillos, una elipse es la forma que se obtiene al "aplastar" un círculo. Es importante en matemáticas y física debido a sus propiedades en órbitas planetarias y en óptica.</p>

        <img src="imgelipse/img2.jpg" alt="elipse">

        <p><strong>Definición:</strong> Una elipse se define como el conjunto de todos los puntos en un plano cuya suma de las distancias a dos puntos fijos (llamados focos) es constante.</p>

        <p><strong>Ecuación estándar:</strong> Dependiendo de la orientación de la elipse, su ecuación puede presentarse en dos formas:</p>

        <p>Elipse horizontal (eje mayor horizontal): (x−h)²/a² + (y−k)²/b² = 1</p>
        <p>Elipse vertical (eje mayor vertical): (x−h)²/b² + (y−k)²/a² = 1</p>

        <p>Donde: (h, k) es el centro de la elipse. <br>
        a es la longitud del semi-eje mayor. <br>
        b es la longitud del semi-eje menor.</p>

        <p>La relación c² = a² − b² permite hallar la distancia c desde el centro a cada foco.</p>

        <p><strong>Propiedades importantes:</strong></p>
        <p>Focos: Cada elipse tiene dos focos, F1 y F2.<br>
        Eje mayor: recta que pasa por los focos, longitud = 2a.<br>
        Eje menor: perpendicular al eje mayor, longitud = 2b.<br>
        Excentricidad: e = √(1 - (b² / a²))</p>

        <p>Mientras más cerca de 0 está la excentricidad, más se parece a un círculo.</p>

        <img src="imgelipse/img3.jpg" alt="elipse">
        <img src="imgelipse/img4.webp" alt="elipse">

        <p><strong>Ejemplo en la vida real:</strong></p>
        <p>Órbitas elípticas de los planetas alrededor del sol.<br>
        Óptica: espejos elípticos concentran la luz en un punto focal.<br>
        Ingeniería y arquitectura en diseño de curvas suaves.</p>

        <p><strong>Visualización:</strong> Si dibujamos una elipse horizontal con a=5 y b=3, la ecuación sería: x²/25 + y²/9 = 1</p>

        <p><strong>Resumen:</strong> La elipse es una curva cerrada y simétrica, con propiedades importantes en geometría, física y otras ciencias exactas. La clave es entender su definición en términos de distancia constante a los focos.</p>

