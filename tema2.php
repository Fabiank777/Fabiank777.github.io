        <h1 class="titulo" id="ancla">vectores</h1>
        <p>¿Qué es un vector?</p>
        <p>En física y matemáticas, un vector es un segmento de una línea recta, dotado de un sentido, es decir, orientado dentro de un plano euclidiano bidimensional o tridimensional. O lo que es lo mismo: un vector es un elemento en un espacio vectorial.</p>
        <img src="imgvec/img1.jpg">

        <p>Dirección. Definida como la recta sobre la cual se traza el vector, continuada infinitamente en el espacio.</p>
        <p>Módulo o amplitud. La longitud gráfica que equivale, dentro de un plano, a la magnitud del vector expresada numéricamente.
</p>
        <p>Sentido. Representado por la punta de la flecha que gráficamente representa al vector, indica el lugar geométrico hacia el cual se dirige el vector.
</p>
        <p>Punto de aplicación. Correspondiente al lugar o punto geométrico en donde inicia el vector gráficamente.
</p>
        <p>Nombre o denominación. Representado mediante una letra que acompaña al vector gráficamente representado, y que coincide con la magnitud que expresa o con la suma de los puntos de inicio y fin de su valor.
</p>
        <p>Sentido de un vector
</p>
        <p>El sentido de los vectores se representa gráficamente mediante una punta de flecha apuntando en alguna dirección. Esto representa hacia qué lado de la línea de acción (dirección) se dirige el vector, o sea, hacia dónde apunta.
</p>

        <p><strong>Tipos de vectores
</strong>Según la ubicación de su punto de aplicación, los vectores se clasifican en:
</p>

        <p>Vectores libres. Aquellos que no poseen un punto de aplicación particular.
</p>
        <p>Vectores deslizantes. Aquellos cuyo punto de aplicación puede ser uno cualquiera a lo largo de la recta de aplicación</p>

        <p>Vectores fijos o ligados. Aquellos que poseen un único y determinado punto de aplicación.
Sin embargo, también es posible clasificar los vectores según otros elementos, de la siguiente manera:</p>


        <p><strong>Vectores angulares o concurrentes.:</strong> Aquellos que forman ángulos respecto de sus líneas de acción o direcciones.
</p>
        
        <p><strong>Vectores opuestos</strong> Aquellos que poseen igual magnitud pero sentido contrario.
</p>

        

        <p><strong>Vectores colineales</strong>Aquellos que comparten recta de acción.
Vectores paralelos. Aquellos cuyas líneas de acción sean, justamente, paralelas.
</p>
       

        <p><strong>Vectores coplanarios.</strong>Aquellos cuyas rectas de acción estén situadas en un mismo plano.
</p>
       
